public class ploblems {
    private double a, b, c, d;
    private String sol;

    public ploblems(double a, double b, double c, double d, String sol) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
        this.sol = sol;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }

    public double getC() {
        return c;
    }

    public double getD() {
        return d;
    }

    public String getSol() {
        return sol;
    }
}